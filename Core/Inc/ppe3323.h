#ifndef INC_PPE3323_H_
#define INC_PPE3323_H_

#include "main.h"

#include <stdbool.h>
#include <stdint.h>


/*
 * Состояние неблокирующего драйвера PPE-3323.
 *
 * Драйвер не ожидает окончание передачи в цикле и не использует HAL_Delay().
 * Это важно, потому что будущая работа с источником не должна нарушать
 * временную структуру двухпроводного протокола.
 */
typedef enum
{
    PPE3323_STATE_NOT_INITIALIZED = 0U,
    PPE3323_STATE_IDLE,
    PPE3323_STATE_QUEUED,
    PPE3323_STATE_TRANSMITTING,
    PPE3323_STATE_ERROR
} ppe3323_state_t;


/*
 * Инициализация драйвера.
 *
 * Функция не передаёт команду источнику.
 * Она только запоминает UART, через который подключён MAX3232.
 */
void ppe3323_init(UART_HandleTypeDef *uart);


/*
 * Фоновая функция драйвера.
 *
 * Должна вызываться из основного цикла main().
 * Она запускает неблокирующую передачу ранее подготовленной команды.
 */
void ppe3323_poll(void);


/*
 * Подготовить группу команд для OUT1:
 *
 * OCP1;
 * VSET1 <voltage>.00;
 * ISET1 <current>;
 *
 * Напряжение задаётся в вольтах.
 * Ток задаётся в миллиамперах.
 *
 * Сама передача начинается в ppe3323_poll().
 */
bool ppe3323_queue_phase(uint8_t voltage_v, uint16_t current_ma);


/*
 * Подготовить команду включения выхода источника.
 *
 * Включение разрешается только после успешной передачи
 * хотя бы одной команды ppe3323_queue_phase().
 */
bool ppe3323_queue_output_on(void);


/*
 * Подготовить команду выключения выхода источника.
 *
 * OUT0 выключает выход источника. В будущем при тревоге
 * ключ линии отключается сразу GPIO-сигналом, а эта команда
 * дополнительно выключает сам PPE-3323.
 */
bool ppe3323_queue_output_off(void);


/*
 * Возвращает 1, если команда уже передаётся или ожидает передачи.
 */
bool ppe3323_is_busy(void);


/*
 * Возвращает текущее состояние драйвера.
 */
ppe3323_state_t ppe3323_get_state(void);


/*
 * Возвращает короткое текстовое имя состояния.
 * Используется только для вывода через COM-порт.
 */
const char *ppe3323_state_text(void);


/*
 * Возвращает 1, если источник уже получил корректные
 * параметры напряжения и ограничения тока.
 */
bool ppe3323_has_phase_configuration(void);

/*
 * Возвращает 1, если команда OUT1 успешно передана по UART.
 *
 * Это программный признак драйвера. Он не измеряет реальное
 * напряжение на выходе источника.
 */
bool ppe3323_output_is_on(void);


/*
 * Возвращает 1 при ошибке UART или ошибке запуска передачи.
 */
bool ppe3323_has_error(void);

/*
 * Эти функции вызываются из HAL callback-функций main.c.
 */
void ppe3323_uart_tx_complete_callback(UART_HandleTypeDef *uart);
void ppe3323_uart_error_callback(UART_HandleTypeDef *uart);

#endif /* INC_PPE3323_H_ */
